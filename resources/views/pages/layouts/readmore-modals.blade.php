			</div>
			<div class="modal-body">
				<p>This five-day intensive training camp took place in Sousse from December 19-24 2016. The camp brought together 60 gender balanced participants from Sfax and Gabes equally. The trainees had theoretical as well as practical session on 3 modules: communication, employability and entrepreneurship. In communication, the workshop focused on interpersonal communication, public speaking, and team work. As for the employability workshop, participants learned how to write a professional CV, answer job interview questions, and search jobs online in Tunisia. The camp ended with the third module which is Entrepreneurship, during which they learned how develop their ideas into concrete projects.</p>
